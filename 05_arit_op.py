szam1=21
szam2=5

print(szam1+szam2)
print(szam1-szam2)
print(szam1*szam2)
print(szam1/szam2)
print(szam1//szam2) #egész számos osztás, nem ad vissza maradékot
print(szam1**szam2) #hatványozó, pl szam1 a szam2-ik hatványon
print(szam1%szam2) #modulo, maradékos osztás

